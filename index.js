app.get('/users', (req, res) => {
    //use query parameter
    const search = req.query.search;

    if (search) {
        const searchResult = users.filter(user => user.name.toLowerCase().includes(search));
        res.send(searchResult);
    }
    else {
        res.send(users);
    }
});

//post method
